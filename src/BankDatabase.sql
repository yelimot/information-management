CREATE TABLE ACCTYPE
(ACCOUNT_TYPE NUMBER(1) NOT NULL PRIMARY KEY,
INTEREST_RATE NUMBER(4,2) NOT NULL,
CONSTRAINT CHECK_INTEREST_RATE_ACCTYPE CHECK (INTEREST_RATE >= 0));

CREATE TABLE ZIP
(ZIP_CODE VARCHAR2(5) NOT NULL PRIMARY KEY,
CITY VARCHAR2(15) NOT NULL,
STREET_NAME VARCHAR2(20) NOT NULL);

CREATE TABLE LOANTYPE
(LOAN_TYPE VARCHAR2(30) NOT NULL PRIMARY KEY,
INTEREST_RATE NUMBER(4,2) NOT NULL, 
LOAN_DESCRIPTION VARCHAR2(40),
CONSTRAINT CHECK_INTEREST_RATE_LOANTYPE CHECK (INTEREST_RATE >= 0));

CREATE TABLE Bank_Branch
(BRANCH_ID VARCHAR2(5) NOT NULL UNIQUE,
STREET_NO VARCHAR2(7) NOT NULL,
APT_NO VARCHAR2(3) NOT NULL,
STREET_NAME VARCHAR2(20) NOT NULL,
ZIP_CODE VARCHAR2(5) NOT NULL,
CONSTRAINT FK_ZIP_CODE_BANK_BRANCH FOREIGN KEY (ZIP_CODE) REFERENCES ZIP(ZIP_CODE)); 

CREATE TABLE Banker
(BANKER_ID VARCHAR2(5) NOT NULL PRIMARY KEY,
BANKER_EMAIL VARCHAR2(255) NOT NULL,
FIRST_NAME VARCHAR2(255) NOT NULL,
LAST_NAME VARCHAR2(255) NOT NULL,
PHONE VARCHAR2(11) NOT NULL,
BRANCH_ID VARCHAR2(5) NOT NULL,
CONSTRAINT FK_BRANCH_ID_BANKER FOREIGN KEY (BRANCH_ID) REFERENCES Bank_Branch(BRANCH_ID));

CREATE TABLE CUSTOMER
(CUST_ID NUMBER(11) NOT NULL PRIMARY KEY,
FIRST_NAME VARCHAR2(15) NOT NULL,
LAST_NAME VARCHAR2(15) NOT NULL,
STREET_NAME VARCHAR2(20) NOT NULL,
STREET_NO VARCHAR2(7) NOT NULL,
APT_NO NUMBER(3) NOT NULL,
EMAIL  VARCHAR2(30),
GENDER VARCHAR2(6) NOT NULL,
BIRTH_DATE DATE NOT NULL,
ZIP_CODE VARCHAR(5) NOT NULL,
BANKER_ID VARCHAR2(5) NOT NULL,
CONSTRAINT FK_ZIP_CODE_CUSTOMER FOREIGN KEY (ZIP_CODE) REFERENCES ZIP(ZIP_CODE),
CONSTRAINT FK_BANKER_ID_CUSTOMER FOREIGN KEY (BANKER_ID) REFERENCES Banker(BANKER_ID));

CREATE TABLE PHONES
(CUST_ID NUMBER(11) NOT NULL,
PHONE_NO NUMBER(10) NOT NULL,
CONSTRAINT FK_CUST_ID_PHONES FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID));

CREATE TABLE ACCOUNT
(ACCOUNT_NUMBER VARCHAR2(8) NOT NULL PRIMARY KEY,
IBAN VARCHAR2(26) NOT NULL,
BALANCE NUMBER(16),
OPEN_DATE date NOT NULL,
BRANCH_ID VARCHAR2(5) NOT NULL,
CUST_ID NUMBER(11) NOT NULL,
ACCOUNT_TYPE NUMBER(1) NOT NULL,
CONSTRAINT FK_BRANCH_ID_ACCOUNT FOREIGN KEY (BRANCH_ID) REFERENCES Bank_Branch(BRANCH_ID),
CONSTRAINT FK_CUST_ID_ACCOUNT FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
CONSTRAINT FK_ACCOUNT_TYPE_ACCOUNT FOREIGN KEY (ACCOUNT_TYPE) REFERENCES ACCTYPE(ACCOUNT_TYPE));

CREATE TABLE CARD
(CARD_ID NUMBER(16) NOT NULL PRIMARY KEY,
CARD_TYPE VARCHAR2(20) NOT NULL,
PASSWORD NUMBER(6) NOT NULL,
EXPIRE_DATE DATE NOT NULL,
CVC VARCHAR2(3) NOT NULL,
ACCOUNT_NUMBER VARCHAR2(8) NOT NULL,
CONSTRAINT FK_ACCOUNT_NUMBER_CARD FOREIGN KEY (ACCOUNT_NUMBER) REFERENCES ACCOUNT(ACCOUNT_NUMBER));

CREATE TABLE LOAN
(LOAN_ID NUMBER(10) NOT NULL PRIMARY KEY,
LOAN_TYPE VARCHAR2(30) NOT NULL,
AMOUNT NUMBER(10,2) NOT NULL,
DATE_OF_LOAN DATE NOT NULL,
DURATION NUMBER(3) NOT NULL,
BRANCH_ID VARCHAR2(8) NOT NULL ,
CUST_ID NUMBER(11) NOT NULL,
CONSTRAINT FK_BRANCH_ID_LOAN FOREIGN KEY (BRANCH_ID) REFERENCES Bank_Branch(BRANCH_ID),
CONSTRAINT FK_CUST_ID_LOAN FOREIGN KEY (CUST_ID) REFERENCES CUSTOMER(CUST_ID),
CONSTRAINT FK_LOAN_TYPE_LOAN FOREIGN KEY (LOAN_TYPE) REFERENCES LOANTYPE(LOAN_TYPE),
CONSTRAINT CHECK_AMOUNT_LOAN CHECK (AMOUNT > 0));

CREATE TABLE TRANSACTION
(TRANSACTION_ID NUMBER(10) NOT NULL PRIMARY KEY,
TRANSACTION_DATE DATE NOT NULL,
AMOUNT NUMBER(9) NOT NULL,
TRANSACTION_TYPE VARCHAR2(26) NOT NULL,
SENDER_ID NUMBER(11) NOT NULL,
RECEIVER_ID NUMBER(11) NOT NULL,
ACCOUNT_NUMBER VARCHAR2(8) NOT NULL,
CONSTRAINT FK_CUST_ID_TRANSACTION FOREIGN KEY (SENDER_ID) REFERENCES CUSTOMER(CUST_ID),
CONSTRAINT FK_CUST_ID_TRANSACTION2 FOREIGN KEY (RECEIVER_ID) REFERENCES CUSTOMER(CUST_ID),
CONSTRAINT FK_ACCOUNT_NUMBER_TRANSACTION FOREIGN KEY (ACCOUNT_NUMBER) REFERENCES ACCOUNT(ACCOUNT_NUMBER),
CONSTRAINT CHECK_AMOUNT_TRANSACTION CHECK (AMOUNT > 0));





